class Breakfast {
  contructor(food, drink) {
    this.food = food;
    this.drink = drink;
  }
}

class Lunch {
  contructor(salad, soup, drink) {
    this.salad = salad
    this.soup = soup
    this.drink = drink
  }
}

class Dinner {
  contructor(salad, soup, entree, dessert) {
    this.salad = salad
    this.soup = soup
    this.entree = entree
    this._dessert = dessert
  }
}